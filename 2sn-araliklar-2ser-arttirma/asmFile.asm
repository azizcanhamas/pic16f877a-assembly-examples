;Timer0 kullanarak 2sn. araliklarla PORTD'yi arttiran sayici uygulamasi
    
    #include <p16f877A.inc>
    
    TMR0_COUNTER EQU 0X20
    
    ORG 0X00
    GOTO SETUP
    
    ORG 0X04
    GOTO KESME
    
    KESME
	BCF INTCON,T0IF
	MOVLW 0X06
	MOVWF TMR0
	DECFSZ TMR0_COUNTER,1
	RETFIE
	MOVLW 0XFA
	MOVWF TMR0_COUNTER
	
	INCF PORTD,1
	INCF PORTD,1
	
	RETFIE
    
    SETUP
	BANKSEL TRISD
	CLRF TRISD
	BANKSEL PORTD
	CLRF PORTD
	
	BANKSEL OPTION_REG
	MOVLW 0X04
	MOVWF OPTION_REG
	
	BANKSEL PORTB
	BSF INTCON,GIE
	BSF INTCON,T0IE
	
	MOVLW 0X06
	MOVWF TMR0
	
	MOVLW 0XFA
	MOVWF TMR0_COUNTER
	
	GOTO MAIN
	
    MAIN
	GOTO MAIN
	
    END


