;Timer0 ile PORTD'ye bagli LED'leri 0,5sn. araliklarla kaydiran program
    
    #include <p16f877A.inc>
    
    TMR0_COUNTER EQU 0X20
    YON EQU 0X21
    ORG 0X00
    GOTO SETUP
    
    ORG 0X04
    GOTO KESME
    
    KESME
	BCF INTCON,T0IF
	MOVLW 0X06
	MOVWF TMR0
	DECFSZ TMR0_COUNTER,1
	RETFIE
	MOVLW 0XFA
	MOVWF TMR0_COUNTER
	
	BTFSC YON,0
	GOTO XX
	
	RLF PORTB,1
	BTFSC PORTB,7
	BSF YON,0
	RETFIE
	
	XX:
	RRF PORTB,1
	BTFSC PORTB,0
	BCF YON,0
	
	
	RETFIE
    
    SETUP
	BANKSEL TRISB
	CLRF TRISB
	BANKSEL PORTB
	CLRF PORTB
	
	MOVLW 0X07
	MOVWF PORTB
	
	BANKSEL OPTION_REG
	MOVLW 0X01
	MOVWF OPTION_REG
	
	BANKSEL PORTB
	BSF INTCON,GIE
	BSF INTCON,T0IE
	
	MOVLW 0X06
	MOVWF TMR0
	
	MOVLW 0XFA
	MOVWF TMR0_COUNTER
	
	BCF STATUS,C
	CLRF YON
	
	GOTO MAIN
	
    MAIN
	GOTO MAIN
	
    END


