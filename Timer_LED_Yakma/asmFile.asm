#include <p16f877A.inc>
    
    TMR0_COUNTER EQU 0X20
    
    ORG 0X00
    GOTO SETUP
    
    ORG 0X04
    GOTO KESME
    
    KESME
	BCF INTCON,T0IF
	MOVLW 0X06
	MOVWF TMR0
	DECFSZ TMR0_COUNTER,1
	RETFIE
	
	MOVLW .100
	MOVWF TMR0_COUNTER
	
	MOVLW 0XFF
	XORWF PORTB,1
	
	RETFIE
    
    SETUP
	BANKSEL TRISB
	CLRF TRISB
	BANKSEL PORTB
	CLRF PORTB
	
	BANKSEL OPTION_REG
	MOVLW 0X03
	MOVWF OPTION_REG
	
	BANKSEL PORTB
	
	BSF INTCON,GIE
	BSF INTCON,T0IE
	
	MOVLW 0X06
	MOVWF TMR0
	
	MOVLW .100
	MOVWF TMR0_COUNTER
	
	GOTO MAIN
	
    MAIN
	GOTO MAIN
	
    END


