#include <p16f877A.inc>
    
    SAYI1_L EQU 0X20
    SAYI1_H EQU 0X21
 
    SAYI2_L EQU 0X22
    SAYI2_H EQU 0X23
    
    ORG 0X00
    GOTO SETUP
    
    SETUP 
	BANKSEL TRISB
	CLRF TRISB
	CLRF TRISC
	BANKSEL PORTB
	CLRF PORTB
	CLRF PORTC
	
	GOTO MAIN
	
    MAIN
	MOVLW 0X0C
	MOVWF SAYI2_L
	
	MOVLW 0X22
	MOVWF SAYI2_H
	
	MOVLW 0XAE
	MOVWF SAYI1_L
	
	MOVLW 0X12
	MOVWF SAYI1_H
	
	MOVFW SAYI1_L
	SUBWF SAYI2_L,1
	BTFSS STATUS,C
	DECF SAYI2_H,1
	MOVFW SAYI1_H
	SUBWF SAYI2_H,1
	
	MOVFW SAYI2_L
	MOVWF PORTB
	
	MOVFW SAYI2_H
	MOVWF PORTC
	
    
	
	GG
	    GOTO GG
	    
    END


