#include <p16f877A.inc>
    
    SAYI1_1 EQU 0X30
    SAYI1_2 EQU 0X31
    SAYI1_3 EQU 0X32
    SAYI1_4 EQU 0X33
 
    SAYI2_1 EQU 0X34
    SAYI2_2 EQU 0X35
    SAYI2_3 EQU 0X36
    SAYI2_4 EQU 0X37
 
    DONGU EQU 0X38
    TMP_ADRES EQU 0X39
    TMP_VERI EQU 0X40
    
    ORG 0X00
    GOTO BASLA
    
    
    BASLA
	MOVLW 0XA
	MOVWF SAYI1_1
	
	MOVLW 0XB
	MOVWF SAYI1_2
	
	MOVLW 0XC
	MOVWF SAYI1_3

	MOVLW 0XD
	MOVWF SAYI1_4
	
	MOVLW 0XD
	MOVWF SAYI4_1
	
	MOVLW 0XC
	MOVWF SAYI3_2
	
	MOVLW 0XB
	MOVWF SAYI2_3

	MOVLW 0XA
	MOVWF SAYI1_4
	
	MOVLW 0X08
	MOVWF DONGU
	
	MOVLW .30
	MOVWF TMP_ADRES
	
	CLRF VERI
	
	LOOP:
	    BCF STATUS,IRP
	    MOVFW TMP_ADRES
	    MOVWF FSR
	    MOVFW INDF
	    MOVWF TMP_VERI
	    BSF STATUS,IRP
	    MOVFW TMP_ADRES
	    MOVWF FSR
	    MOVFW TMP_VERI
	    MOVWF INDF
	    INCF TMP_ADRES,1
	    DECFSZ LOOP,1
	    GOTO LOOP
	
	LOOP
    
	
	
	GG
	    GOTO GG
	    
	END


